// Relational or comparison operators

// > - more than
// < - less than
// >= - more than equal
// <= - less than equal

// will return a boolean
var result = 10 > 5
console.log(result)

// equality operators

var x = 1
console.log(x == '1') //true we compare value on the right to left // lose comparison
console.log(x === '1') //false // strick comparison / we are not only checking the value but also the data type
console.log(x === 1) //true